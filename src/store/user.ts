import type { User } from '@/api/interface/user'
import { defineStore } from 'pinia'

interface UserStore {
	user: User | null
	token: string | null
}

const useUserStore = defineStore('user', {
	state: (): UserStore => {
		return {
			user: null,
			token: null
		}
	},
	actions: {}
})

export default useUserStore
